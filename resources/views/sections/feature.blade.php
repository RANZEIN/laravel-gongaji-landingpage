<script>
document.addEventListener('DOMContentLoaded', function() {
    // Simple parallax effect for features sections
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;

        if (window.innerWidth > 768) {
            // Features CTA parallax
            const featuresSection = document.querySelector('.features-cta');
            if (featuresSection) {
                const speed = scrolled * 0.3;
                featuresSection.style.backgroundPosition = `center ${speed}px`;
            }

            // Bottom CTA parallax
            const bottomSection = document.querySelector('.bottom-cta');
            if (bottomSection) {
                const speed = scrolled * 0.2;
                bottomSection.style.backgroundPosition = `center ${speed}px`;
            }
        }
    });
});
</script>
